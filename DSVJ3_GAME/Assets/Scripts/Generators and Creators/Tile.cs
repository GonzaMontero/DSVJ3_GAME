public class Tile
{
    int x;
    int y;

    public int X
    {
        get
        {
            return x;
        }
    }
    public int Y
    {
        get
        {
            return y;
        }
    }

    public Tile( int x, int y)
    {
        this.x = x;
        this.y = y;
    }
}
